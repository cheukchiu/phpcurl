class EasyResponseTest extends PHPUnit_Framework_TestCase
{
    public function testGetSet()
    {
        $response = new \easyops\curl\EasyResponse('', [], '{"a":1,"b":2,"c":3}', ['http_code' => 200, 'content_type' => 'application/json', 'total_time' => 0.001]);
        $this->assertArraySubset(['a'=>1, 'b' => 2, 'c' => 3], $response->getBody(true));
    }

}
